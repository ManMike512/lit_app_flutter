// Step 1: Create a Dart class that represents your data
class Category {
  final int id;
  final int? language;
  final String ldesc;
  final String name;
  final String pageUrl;
  final String sdesc;
  final String topUrl;
  final String type;
  final int submissionCount;

  Category({
    required this.id,
    this.language,
    required this.ldesc,
    required this.name,
    required this.pageUrl,
    required this.sdesc,
    required this.topUrl,
    required this.type,
    required this.submissionCount,
  });

  factory Category.fromJson(Map<String, dynamic> json) {
    return Category(
      id: json['id'],
      language: json['language'],
      ldesc: json['ldesc'],
      name: json['name'],
      pageUrl: json['pageUrl'],
      sdesc: json['sdesc'],
      topUrl: json['topUrl'],
      type: json['type'],
      submissionCount: json['submission_count'],
    );
  }
}

